from rest_framework import permissions


class IsOwner(permissions.BasePermission):
    def has_permission(self, request, view):
        return request.user.is_authenticated

    def has_object_permission(self, request, view, obj):
        return obj.owner == request.user


class HasProjectAccess(permissions.BasePermission):
    def has_permission(self, request, view):
        return request.user.is_authenticated

    def has_object_permission(self, request, view, obj):
        if obj.owner == request.user:
            return True

        if obj.permissions.filter(user=request.user).exists():
            return True

        return False


class HasTaskAccess(permissions.BasePermission):
    def has_permission(self, request, view):
        return request.user.is_authenticated

    def has_object_permission(self, request, view, obj):
        if obj.owner == request.user:
            return True

        if obj.permissions.filter(user=request.user).exists():
            return True

        if obj.project:
            if obj.project.owner == request.user:
                return True

            if obj.project.permissions.filter(user=request.user).exists():
                return True

        return False


class IsAuthorOrReadOnly(permissions.BasePermission):
    def has_permission(self, request, view):
        return request.user.is_authenticated

    def has_object_permission(self, request, view, obj):
        if request.method in permissions.SAFE_METHODS:
            return True

        return obj.author == request.user


